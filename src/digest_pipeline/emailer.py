"""Email dispatch via SMTP with SSL/TLS and dry-run support.

EARS coverage
─────────────
- Ubiquitous 2.1-4: format final digest as HTML/plaintext email.
- Event 2.2-4: dispatch email to configured recipient after LLM summary.
- State 2.3-2: use encrypted SSL/TLS for SMTP connections.
- State 2.3-3: in dry-run mode, print email to console instead of sending.
"""

from __future__ import annotations

import logging
import smtplib
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText

from jinja2 import Template

from digest_pipeline.config import Settings

logger = logging.getLogger(__name__)

HTML_TEMPLATE = Template("""\
<!DOCTYPE html>
<html>
<head><meta charset="utf-8"><style>
  body { font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif;
         max-width: 700px; margin: auto; padding: 20px; color: #222; }
  h1 { color: #1a1a2e; border-bottom: 2px solid #e2e2e2; padding-bottom: 10px; }
  .summary { line-height: 1.6; white-space: pre-wrap; }
  .footer { margin-top: 30px; font-size: 0.85em; color: #888; }
</style></head>
<body>
  <h1>Daily Research Digest &mdash; {{ date }}</h1>
  <div class="summary">{{ summary }}</div>
  <p class="footer">Generated by the Automated Research Digest Pipeline.
     {{ paper_count }} paper(s) processed.</p>
</body>
</html>
""")

PLAIN_TEMPLATE = Template("""\
Daily Research Digest — {{ date }}
==========================================

{{ summary }}

---
Generated by the Automated Research Digest Pipeline.
{{ paper_count }} paper(s) processed.
""")


def _build_email(summary: str, paper_count: int, date_str: str, settings: Settings) -> MIMEMultipart:
    msg = MIMEMultipart("alternative")
    msg["Subject"] = f"Research Digest — {date_str}"
    msg["From"] = settings.email_from
    msg["To"] = settings.email_to

    ctx = {"summary": summary, "paper_count": paper_count, "date": date_str}
    msg.attach(MIMEText(PLAIN_TEMPLATE.render(**ctx), "plain"))
    msg.attach(MIMEText(HTML_TEMPLATE.render(**ctx), "html"))
    return msg


def send_digest(
    summary: str,
    paper_count: int,
    date_str: str,
    settings: Settings,
) -> None:
    """Format and send (or print) the daily digest email.

    In dry-run mode the email payload is printed to the console instead
    of being sent (EARS 2.3-3).
    """
    msg = _build_email(summary, paper_count, date_str, settings)

    if settings.dry_run:
        logger.info("DRY-RUN mode — printing email to console.")
        print("=" * 60)
        print(f"To:      {settings.email_to}")
        print(f"From:    {settings.email_from}")
        print(f"Subject: {msg['Subject']}")
        print("=" * 60)
        print(PLAIN_TEMPLATE.render(summary=summary, paper_count=paper_count, date=date_str))
        print("=" * 60)
        return

    logger.info("Sending digest email via %s:%d (SSL/TLS).", settings.smtp_host, settings.smtp_port)
    with smtplib.SMTP_SSL(settings.smtp_host, settings.smtp_port) as server:
        server.login(settings.smtp_user, settings.smtp_password)
        server.send_message(msg)
    logger.info("Email sent to %s.", settings.email_to)
